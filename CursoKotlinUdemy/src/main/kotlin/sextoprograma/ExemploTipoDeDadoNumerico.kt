fun main(){

    // Byte, Int, Short, Long
    val byte: Byte = 127
    val int: Int = 1000
    val short: Short = -32768
    val long: Long = 3000

    print("Byte: $byte\n")
    print("Byte valor Min: ${Byte.MIN_VALUE}\n")
    print("Byte valor Max: ${Byte.MAX_VALUE}\n")

    print("Int: $int\n")
    print("Int valor Min: ${Int.MIN_VALUE}\n")
    print("Int valor Max: ${Int.MAX_VALUE}\n")

    print("Short: $short\n")
    print("Short valor Min: ${Short.MIN_VALUE}\n")
    print("Short valor Max: ${Short.MAX_VALUE}\n")

    print("Long: $long\n")
    print("Long valor Min: ${Long.MIN_VALUE}\n")
    print("Long valor Max: ${Long.MAX_VALUE}\n")


}